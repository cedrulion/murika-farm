// Create product
exports.createProduct = async (req, res) => {
  try {
    const { category, vendor, quantity, price, isKG, isDozen } = req.body;

    // Handle the uploaded file
    const image = req.file ? req.file.path : null; // Get the file path

    const product = await Product.create({
      category,
      vendor,
      quantity,
      isKG,
      isDozen,
      price,
      image, // Save the image path
      salesPerDay: 0,
      expectedSales: 0,
      revenue: 0
    });

    res.status(201).json({
      success: true,
      data: product
    });
  } catch (error) {
    if (error.name === 'ValidationError') {
      const messages = Object.values(error.errors).map(err => err.message);
      return res.status(400).json({
        success: false,
        error: messages
      });
    }
    res.status(500).json({
      success: false,
      error: 'Server Error'
    });
  }
};

// Update product
exports.updateProduct = async (req, res) => {
  try {
    let product = await Product.findById(req.params.id);
    if (!product) {
      return res.status(404).json({
        success: false,
        error: 'Product not found'
      });
    }

    // Handle the uploaded file
    const image = req.file ? req.file.path : product.image; // Keep the existing image if no new file is uploaded

    product = await Product.findByIdAndUpdate(req.params.id, {
      ...req.body,
      image // Update the image path
    }, {
      new: true,
      runValidators: true
    });

    res.status(200).json({
      success: true,
      data: product
    });
  } catch (error) {
    if (error.name === 'ValidationError') {
      const messages = Object.values(error.errors).map(err => err.message);
      return res.status(400).json({
        success: false,
        error: messages
      });
    }
    res.status(500).json({
      success: false,
      error: 'Server Error'
    });
  }
};

// Delete product
exports.deleteProduct = async (req, res) => {
  try {
    const product = await Product.findById(req.params.id);
    if (!product) {
      return res.status(404).json({
        success: false,
        error: 'Product not found'
      });
    }

    await product.remove();

    res.status(200).json({
      success: true,
      data: {}
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      error: 'Server Error'
    });
  }
};